package iscs.siano.oggetto;

public class Oggetto {
	
    private String oggettoParticolare;
	
	private String proprietà;

	public String getOggettoParticolare() {
		return oggettoParticolare;
	}

	public void setOggettoParticolare(String oggettoParticolare) {
		this.oggettoParticolare = oggettoParticolare;
	}

	public String getProprietà() {
		return proprietà;
	}

	public void setProprietà(String proprietà) {
		this.proprietà = proprietà;
	}

}
